#include "export/result_formatter.h"

#include <stdio.h>

#include "utils/xray_logger.h"

static void result_formatter_write_json_escaped_string(
    FILE *file,
    const char *text
)
{
    fputc('"', file);

    for (const char *cursor = text; *cursor != '\0'; ++cursor)
    {
        if (*cursor == '"' || *cursor == '\\')
        {
            fputc('\\', file);
        }

        fputc(*cursor, file);
    }

    fputc('"', file);
}

XrayStatus result_formatter_write_json(
    const XrayAnalysisResult *result,
    const char *output_path
)
{
    if ((result == NULL) || (output_path == NULL))
    {
        return XRAY_STATUS_INVALID_ARGUMENT;
    }

    FILE *file = fopen(output_path, "w");

    if (file == NULL)
    {
        xray_log(
            XRAY_LOG_LEVEL_ERROR,
            "RESULT_FORMATTER",
            "Failed to open JSON report output: %s",
            output_path
        );

        return XRAY_STATUS_EXPORT_FAILED;
    }

    fprintf(file, "{\n");
    fprintf(file, "  \"study_type\": ");
    result_formatter_write_json_escaped_string(file, result->study_type);
    fprintf(file, ",\n");

    fprintf(file, "  \"image_path\": ");
    result_formatter_write_json_escaped_string(file, result->image_path);
    fprintf(file, ",\n");

    fprintf(file, "  \"inference_completed\": %s,\n", result->inference_completed ? "true" : "false");

    fprintf(file, "  \"warning\": ");
    result_formatter_write_json_escaped_string(file, result->warning);
    fprintf(file, ",\n");

    fprintf(file, "  \"finding_count\": %llu,\n", (unsigned long long)result->finding_count);
    fprintf(file, "  \"findings\": [\n");

    for (size_t i = 0U; i < result->finding_count; ++i)
    {
        const FindingResult *finding = &result->findings[i];

        fprintf(file, "    {\n");

        fprintf(file, "      \"label\": ");
        result_formatter_write_json_escaped_string(file, finding->label);
        fprintf(file, ",\n");

        fprintf(file, "      \"probability\": %.9f,\n", (double)finding->probability);

        fprintf(file, "      \"status\": ");
        result_formatter_write_json_escaped_string(file, finding->status);
        fprintf(file, ",\n");

        fprintf(file, "      \"note\": ");
        result_formatter_write_json_escaped_string(file, finding->note);
        fprintf(file, ",\n");

        fprintf(
            file,
            "      \"doctor_review_required\": %s\n",
            finding->doctor_review_required ? "true" : "false"
        );

        fprintf(file, "    }%s\n", (i + 1U < result->finding_count) ? "," : "");
    }

    fprintf(file, "  ]\n");
    fprintf(file, "}\n");

    fclose(file);

    xray_log(
        XRAY_LOG_LEVEL_INFO,
        "RESULT_FORMATTER",
        "JSON report written: %s",
        output_path
    );

    return XRAY_STATUS_SUCCESS;
}

XrayStatus result_formatter_print_console(
    const XrayAnalysisResult *result
)
{
    if (result == NULL)
    {
        return XRAY_STATUS_INVALID_ARGUMENT;
    }

    printf("\n");
    printf("============================================================\n");
    printf("AI-Assisted Chest X-Ray Analysis Report\n");
    printf("============================================================\n");
    printf("Study Type: %s\n", result->study_type);
    printf("Image Path : %s\n", result->image_path);
    printf("Completed  : %s\n", result->inference_completed ? "yes" : "no");
    printf("Warning    : %s\n", result->warning);
    printf("Findings   : %llu\n", (unsigned long long)result->finding_count);
    printf("------------------------------------------------------------\n");

    if (result->finding_count == 0U)
    {
        printf("No primary suspicious findings generated by the rule engine.\n");
    }

    for (size_t i = 0U; i < result->finding_count; ++i)
    {
        const FindingResult *finding = &result->findings[i];

        printf(
            "%llu. %s | probability=%.6f | status=%s\n",
            (unsigned long long)(i + 1U),
            finding->label,
            (double)finding->probability,
            finding->status
        );

        printf("   Note: %s\n", finding->note);
    }

    printf("============================================================\n");
    printf("\n");

    return XRAY_STATUS_SUCCESS;
}
